print("Konversi Suhu dari Celcius ke Kelvin, Reamur dan Fahrenheit")
c=float(input("Masukkan suhu dalam Celcius : "))

print(f"Hasil konversi dari suhu {int(c)} derajat Celcius ke Kelvin adalah : {int(c+273)}K")
print(f"Hasil konversi dari suhu {int(c)} derajat Celcius ke Reamur adalah : {int(c*4/5)}R")
print(f"Hasil konversi dari suhu {int(c)} derajat Celcius ke Fahrenheit adalah : {int((c*9/5)+32)}F")